#!/usr/bin/env node
import process from "node:process";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { renderDependabotYaml, mirrorPolicyForRepo } from "./dependabot-policy.mjs";

const API_VERSION = "2026-03-10";

function parseArgs(argv) {
  const args = { branch: "repo-ops/dependabot-policy", base: "main", merge: false };
  for (let index = 0; index < argv.length; index += 1) {
    const key = argv[index];
    if (!key.startsWith("--")) throw new Error(`Unexpected argument: ${key}`);
    const name = key.slice(2).replace(/-([a-z])/g, (_, c) => c.toUpperCase());
    if (name === "merge") {
      args.merge = argv[index + 1] === "true";
      index += 1;
    } else {
      args[name] = argv[index + 1];
      index += 1;
    }
  }
  if (!args.repo) throw new Error("--repo is required");
  return args;
}

function tokenFor(owner) {
  if (owner === "oaslananka") return process.env.GH_TOKEN_SOURCE || process.env.GH_TOKEN || "";
  if (owner === "oaslananka-lab") return process.env.GH_TOKEN_LAB || process.env.GH_TOKEN || "";
  return process.env.GH_TOKEN || "";
}

async function api(owner, method, endpoint, body, allowFail = false) {
  const token = tokenFor(owner);
  if (!token) throw new Error(`Missing GitHub token for ${owner}`);
  const response = await fetch(`https://api.github.com/${endpoint}`, {
    method,
    headers: {
      Accept: "application/vnd.github+json",
      Authorization: `Bearer ${token}`,
      "Content-Type": "application/json",
      "User-Agent": "oaslananka-repo-ops-dependabot-policy",
      "X-GitHub-Api-Version": API_VERSION,
    },
    body: body === undefined ? undefined : JSON.stringify(body),
  });
  const text = await response.text();
  const data = text ? JSON.parse(text) : null;
  if (!response.ok && !allowFail) {
    const error = new Error(data?.message || `GitHub API ${method} ${endpoint} failed: ${response.status}`);
    error.status = response.status;
    error.data = data;
    throw error;
  }
  return { ok: response.ok, status: response.status, data };
}

async function upsertDependabotFile(target, content, options) {
  const [owner, repo] = target.split("/");
  const baseRef = await api(owner, "GET", `repos/${target}/git/ref/heads/${encodeURIComponent(options.base)}`);
  const branchRef = await api(owner, "GET", `repos/${target}/git/ref/heads/${encodeURIComponent(options.branch)}`, undefined, true);
  if (!branchRef.ok) {
    await api(owner, "POST", `repos/${target}/git/refs`, {
      ref: `refs/heads/${options.branch}`,
      sha: baseRef.data.object.sha,
    });
  }
  const path = ".github/dependabot.yml";
  const existing = await api(
    owner,
    "GET",
    `repos/${target}/contents/${encodeURIComponent(path).replaceAll("%2F", "/")}?ref=${encodeURIComponent(options.branch)}`,
    undefined,
    true,
  );
  const current = existing.ok && existing.data?.content ? Buffer.from(existing.data.content, "base64").toString("utf8") : null;
  const changed = current !== content;
  if (changed) {
    await api(owner, "PUT", `repos/${target}/contents/${encodeURIComponent(path).replaceAll("%2F", "/")}`, {
      message: "ops(dependabot): apply policy-managed config",
      content: Buffer.from(content).toString("base64"),
      branch: options.branch,
      sha: existing.ok ? existing.data.sha : undefined,
    });
  }
  const pulls = await api(
    owner,
    "GET",
    `repos/${target}/pulls?state=open&head=${encodeURIComponent(`${owner}:${options.branch}`)}&base=${encodeURIComponent(options.base)}`,
  );
  let pr = pulls.data[0] || null;
  if (changed && !pr) {
    pr = (
      await api(owner, "POST", `repos/${target}/pulls`, {
        title: "ops(dependabot): apply policy-managed config",
        body:
          "Generated by oaslananka-lab/_ops from repository autonomy policy. The file is intentionally identical on source and mirror; repository settings decide whether Dependabot is active.",
        head: options.branch,
        base: options.base,
        maintainer_can_modify: true,
      })
    ).data;
  }
  let merge = null;
  if (options.merge && pr) {
    const fresh = await api(owner, "GET", `repos/${target}/pulls/${pr.number}`);
    merge = await api(
      owner,
      "PUT",
      `repos/${target}/pulls/${pr.number}/merge`,
      {
        commit_title: "ops(dependabot): apply policy-managed config",
        merge_method: "squash",
        sha: fresh.data.head.sha,
      },
      true,
    );
  }
  return {
    target,
    changed,
    pr: pr ? { number: pr.number, url: pr.html_url, head_sha: pr.head?.sha || null } : null,
    merge: merge
      ? {
          ok: merge.ok,
          status: merge.status,
          sha: merge.data?.sha || null,
          message: merge.data?.message || null,
        }
      : null,
  };
}

export async function main(argv = process.argv.slice(2)) {
  const args = parseArgs(argv);
  const policy = mirrorPolicyForRepo(args.repo);
  const content = renderDependabotYaml(policy);
  const targets = [
    `${policy.mirror.source_owner}/${policy.mirror.source_repo}`,
    `${policy.mirror.mirror_owner}/${policy.mirror.mirror_repo}`,
  ];
  const results = [];
  for (const target of targets) {
    results.push(await upsertDependabotFile(target, content, args));
  }
  process.stdout.write(`${JSON.stringify({ repo: args.repo, targets: results }, null, 2)}\n`);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === path.resolve(process.argv[1])) {
  main().catch((error) => {
    console.error(error.message);
    if (error.data?.message) console.error(error.data.message);
    process.exit(1);
  });
}
