#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import { resolvePolicy } from "./ops-policy.mjs";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

function tokenFor(owner) {
  if (owner === "oaslananka") return process.env.GH_TOKEN_SOURCE || process.env.GH_TOKEN || "";
  if (owner === "oaslananka-lab") return process.env.GH_TOKEN_LAB || process.env.GH_TOKEN || "";
  return process.env.GH_TOKEN || "";
}

function gh(owner, args) {
  const result = spawnSync("gh", args, {
    cwd: ROOT,
    encoding: "utf8",
    env: { ...process.env, GH_TOKEN: tokenFor(owner) },
  });
  if (result.status !== 0) return { ok: false, status: result.status, error: result.stderr.trim() || result.stdout.trim() };
  try {
    return { ok: true, data: result.stdout.trim() ? JSON.parse(result.stdout) : null };
  } catch {
    return { ok: true, data: result.stdout };
  }
}

function mirrorPolicies() {
  const dir = path.join(ROOT, "config", "repos", "oaslananka-lab");
  return fs
    .readdirSync(dir)
    .filter((file) => file.endsWith(".yml"))
    .map((file) => file.slice(0, -4))
    .map((repo) => resolvePolicy("oaslananka-lab", repo))
    .filter((policy) => policy.mirror?.enabled)
    .sort((a, b) => a.mirror.mirror_repo.localeCompare(b.mirror.mirror_repo));
}

export function desiredSecurityState(owner) {
  if (owner === "oaslananka") {
    return {
      actions: "disabled_or_unavailable",
      vulnerability_alerts: "disabled",
      automated_security_fixes: "disabled",
      secret_scanning: "disabled",
      secret_scanning_push_protection: "disabled",
      code_scanning_default_setup: "not_configured",
    };
  }
  return {
    actions: "enabled",
    vulnerability_alerts: "enabled",
    automated_security_fixes: "enabled",
    secret_scanning: "enabled",
    secret_scanning_push_protection: "enabled",
    code_scanning_default_setup: "policy_controlled",
  };
}

function observeRepo(owner, repo) {
  const full = `${owner}/${repo}`;
  const repoData = gh(owner, ["api", `repos/${full}`]);
  const actions = gh(owner, ["api", `repos/${full}/actions/permissions`]);
  const autoFixes = gh(owner, ["api", `repos/${full}/automated-security-fixes`]);
  const codeql = gh(owner, ["api", `repos/${full}/code-scanning/default-setup`]);
  const dependabot = gh(owner, ["api", `repos/${full}/contents/.github/dependabot.yml?ref=main`]);
  const security = repoData.data?.security_and_analysis || {};
  return {
    repository: full,
    desired: desiredSecurityState(owner),
    observed: {
      actions_enabled: actions.data?.enabled ?? null,
      vulnerability_alerts_api: autoFixes.ok ? "reachable" : "unavailable",
      automated_security_fixes_enabled: autoFixes.data?.enabled ?? null,
      secret_scanning: security.secret_scanning?.status ?? "unavailable",
      secret_scanning_push_protection: security.secret_scanning_push_protection?.status ?? "unavailable",
      code_scanning_default_setup: codeql.data?.state ?? "unavailable",
      dependabot_file: dependabot.ok ? "present" : "missing_or_unavailable",
    },
    api_errors: [repoData, actions, autoFixes, codeql, dependabot].filter((item) => !item.ok).map((item) => item.error),
  };
}

export function main() {
  const outDir = path.join(ROOT, "out");
  fs.mkdirSync(outDir, { recursive: true });
  const report = { generated_at: new Date().toISOString(), repos: [] };
  for (const policy of mirrorPolicies()) {
    report.repos.push(observeRepo(policy.mirror.source_owner, policy.mirror.source_repo));
    report.repos.push(observeRepo(policy.mirror.mirror_owner, policy.mirror.mirror_repo));
  }
  fs.writeFileSync(path.join(outDir, "fleet-security-state.json"), `${JSON.stringify(report, null, 2)}\n`);
  fs.mkdirSync(path.join(ROOT, "docs"), { recursive: true });
  fs.writeFileSync(
    path.join(ROOT, "docs", "fleet-security-state.md"),
    `# Fleet Security State\n\nGenerated by \`scripts/fleet-security-state.mjs\`.\n\nSource repositories keep generated security files for source/mirror parity, but security automation is disabled by repository settings. Mirror repositories run Actions and security automation.\n\n`,
  );
  process.stdout.write(`${JSON.stringify(report, null, 2)}\n`);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === path.resolve(process.argv[1])) {
  main();
}
